#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	char	*ptr;
	size_t	i;

	i = 0;
	ptr = (char *)b;
	while (len > 0)
	{
		ptr[i] = c;
		i++;
		len--;
	}
	return (ptr);
}
